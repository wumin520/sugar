<script>
  const removeElement = (marker) => {
    let el = document.querySelector(marker) || document.querySelector(`[${marker}]`)
    if (el) {
      document.body.removeChild(el)
    }
  }

  function preventDefault (e) {
    e.preventDefault()
  }

  export default {
    data () {
      return {
        state: 0,
        tips: '',
        showSpinner: true
      }
    },

    destroyed () {
      console.log('[Vonic] Loading _vm destroyed')
      removeElement('von-loading')
    },

    methods: {
      show (options) {
        this.tips = options.tips
        this.showSpinner = !!options.showSpinner

        this.state = 1
        setTimeout(() => {
          this.state = 2
        })

        document.body.addEventListener('touchmove', preventDefault)
      },

      hide () {
        this.state = 1
        setTimeout(() => {
          this.state = 0
          setTimeout(() => {
            this.$destroy()
          })
        }, 300)

        document.body.removeEventListener('touchmove', preventDefault)
      },

      update (options) {
        this.tips = options.tips
        this.showSpinner = !!options.showSpinner
      },

      getState () {
        return this.state
      }
    }
  }
</script>
